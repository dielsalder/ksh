class Molecule:
    def __init__(self, atoms):
        self.atoms = atoms
        self.fits = []

    def select(self, res = [], atom_name = []):
        """Select atoms by residue and element"""
        selection = (
            [atom for atom in self.atoms if atom['res_name'] in res
                and atom['atom_name'] in atom_name])
        return selection

    def crds_of(self, atoms):
        """Get xyz coordinates of selected atoms"""
        crds = [[atom['x'], atom['y'], atom['z']] for atom in flatten(atoms)]
        return crds

    def write(self, atoms, out = open('atoms.pdb', 'w')):
        """Write selected atoms to pdb"""
        out.write('REMARK      generated by pdb.py\n')
        for atom in atoms:
            vals = (['ATOM', atom['atom_num'], atom['atom_name'],
                atom['res_name'], atom['res_num'],
                atom['x'], atom['y'], atom['z'],
                '1.00', '0.00', '\n'])
            line = '    '.join(str(v) for v in vals)
            out.write(line)

    def minimize(self, selection):
        """fit minimized helix"""
        crds = crds_of(selection)
        fit = Fit(crds)
        min_fit = (fit.minimize())
        self.fits.append(min_fit)
        return min_fit

class Dna(Molecule):
    """Store data for the atoms in one DNA molecule"""
    def __init__(self, atoms):
        self.atoms = atoms
        self.strands()
        self.pairs()
        self.all_crds = self.crds_of(self.atoms)

    def strands(self, n = 1):
        """
        Group atoms by helix strand
        Set n as starf of residue numbering
        """

        prev_atom = {'res_num': n}
        for i, atom in enumerate(self.atoms):
            if (atom['res_num'] != prev_atom['res_num'] and
                    atom['res_num'] == n):
                strand1 = self.atoms[0:i]
                strand2 = self.atoms[i:]
            prev_atom = atom
        for atom in self.atoms:
            if atom in strand1:
                atom['strand'] = 1
            elif atom in strand2:
                atom['strand'] = 2
        return self.atoms

    def pairs(self):
        str1 = [atom for atom in self.atoms if atom['strand'] == 1]
        str2 = [atom for atom in self.atoms if atom['strand'] == 2]
        for i, (a, b) in enumerate(zip(str1, str2)):
            a['pair'] = i
            b['pair'] = i
        return self.atoms

    def get_pairs(self, start = 0, end = 10):
        """Get base pairs"""
        # Both strands are numbered 5' to 3'
        for i in range(start, end):
            yield [atom for atom in self.atoms if atom['pair'] == i]

    def get_pair_crds(self):
        """Coordinates by base pair"""
        for pair in self.get_pairs():
            yield crds_of(pair)

class Fit:
    """Store fit results"""
    def __init__(self, crds):
        self.crdset = ksh.crdset(crds)

    def no_rotate(self):
        """Fit without rotation"""
        fit = self.crdset.calc_all()
        self.no_rotate = fit
        return self.no_rotate

    def minimize(self):
        """Minimize rotation"""
        rotation = ksh.best_rotation(self.crdset)
        best = rotation.calc_all()
        self.best = best
        return self.best

    def write_minimize(self, Molecule):
        """Write minimized fit to selected Molecule"""
        Molecule.fits.append(self.best)

